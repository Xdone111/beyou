public class RedBagReceiveBean {
    @Override
    public String toString() {
        return "RedBagReceiveAdapter{" +
                "msg='" + msg + '\'' +
                ", data='" + data + '\'' +
                ", code='" + code + '\'' +
                '}';
    }

    /**
     * msg : 您已经抢过红包了
     * data :
     * code : 0
     */

    private String msg;
    private String data;
    private String code;

    public String getMsg() {
        return msg;
    }

    public void setMsg(String msg) {
        this.msg = msg;
    }

    public String getData() {
        return data;
    }

    public void setData(String data) {
        this.data = data;
    }

    public String getCode() {
        return code;
    }

    public void setCode(String code) {
        this.code = code;
    }
}
